// How to find size of Data types...
#include<stdio.h>
int main(){
    int a;
    float b;
    char c;
    double d;
    printf("The size of int is %d byte\n",sizeof(a));
    printf("The size of float is %d byte\n",sizeof(b));
    printf("The size of char is %d byte\n",sizeof(c));
    printf("The size of double is %d byte\n",sizeof(d));
    return 0;
}